package com.latis.wpc.dto.group;

public class WpcGroup {
	private String group_id;
	private String create_date;
	private String last_modiefied_date;
	
	
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getCreate_date() {
		return create_date;
	}
	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}
	public String getLast_modiefied_date() {
		return last_modiefied_date;
	}
	public void setLast_modiefied_date(String last_modified_date) {
		this.last_modiefied_date = last_modified_date;
	}
	
	
}
